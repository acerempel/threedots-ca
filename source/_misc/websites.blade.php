@section('content')

    <ul>
      <li><a href="https://jocelynveevers.wordpress.com" title="Jocelyn Veevers – Reading, sewing, pondering"><b>Jocelyn Veevers</b> writes eloquently</a>
        about the diverse array of books she reads each month. An outstanding source for recommendations and reviews
        concerning intriguing books that you're (or at least I’m) not likely to have heard of.
      <li>About once every two months, I spend a day reading a couple years’ worth of posts on <a href="https://humantransit.org"
        title="Human Transit – The professional blog of public transit consultant Jarrett Walker.">Human Transit</a>, the blog of <b>Jarrett Walker</b>, a
        public transit consultant from Portland, Oregon. I always come away in an electric daze of contemplation.
      <li>The <a href="https://www.ursulakleguin.com/blog-archive"
        title="Ursula K. Le Guin: A Blog">blog of the late <b>Ursula K. Le Guin</b></a> is completely wonderful.
        Much delightful, expressive, and perspicacious writing on such topics as her cat
        (Pard), language, books, the internet, politics, general silliness …
      <li>
        <a href="http://www.davidwalbert.com/dw/" title="David Walbert: writer, historian, educator, craftsman">
          <b>David Walbert</b>’s writings</a> on the history of food, on theology, philosophy, and relating musings – I
        find them to be very worthwhile.
        <li><a
            href="https://www.davidlebovitz.com/"
            title="Original Recipes, Books, & Life in Paris – David Lebovitz">
            <b>David Lebovitz</b>’s blog</a> is always a good source for cake recipes and anecdotes about life in Paris.
    </ul>

@endsection
